public class IfElse {
    public static void main(String[] args) {

        boolean check= 5<10;

        if (check==true){
            System.out.println("Verdadero");
        }else if(check==false){
            System.out.println("Falso");
        }else{
            System.out.println("jaja");
        }

    }
}
